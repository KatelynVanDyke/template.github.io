/* Menu */

function openMenu() {
    document.getElementById("nav-mobile-menu").style.display = "none";
    document.getElementById("nav-mobile-close").style.display = "block";
    document.getElementById("nav-mobile-links").style.display = "block";
}

function closeMenu() {
    document.getElementById("nav-mobile-menu").style.display = "block";
    document.getElementById("nav-mobile-close").style.display = "none";
    document.getElementById("nav-mobile-links").style.display = "none";
}